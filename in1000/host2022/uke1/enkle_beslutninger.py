# 01.08 Enkle beslutninger

# a)
if 6 > 5:
    print("IN1000")
# True: Her skrives "IN1000" ut.

# b)
if 6 == 6:
    print("IN1000")
# True: Her skrives "IN1000" ut.

# c)
if 6 < 6:
    print("IN1000")
# False: Her skrives ikke "IN1000" ut.

# d)
if 6 >= 6:
    print("IN1000")
# True: Her skrives "IN1000" ut.

# e)
if 6 == 6:
    print("ingenting")
else:
    print("IN1000")
# Her skrives "ingenting" ut.
