class HeiVerden {
    public static void main(String[] args) {
        System.out.println("Hei Verden!");

        int alder = 4;
        System.out.println("Alder: " + alder);

        // Naa endrer vi verdien av alder til terminalen

        alder = 24; // Trenger ikke deklarere alder med type paa nytt.
        System.out.println("Min alder er: " + alder);
    }
}
