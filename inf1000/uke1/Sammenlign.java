class Sammenlign {
    public static void main(String[] args) {
        int a = -2;
        int b = 6;

        if(a > b) {
            System.out.println("Tall a er storre enn tall b.");
        } else if(a < b) {
            System.out.println("Tall b er storre enn tall a.");
        } else {
            System.out.println("Tall a og b er like store");
        }
    }
}
